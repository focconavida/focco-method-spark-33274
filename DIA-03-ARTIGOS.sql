  <li>Jogos estratégicos ou quebra-cabeças complexos</li>
  <li>Voluntariado que demanda presença total</li>
  <li>Conversas profundas com pessoas queridas</li>
</ul>

<p>Dedique pelo menos 3-5 horas semanais a essas atividades.</p>

