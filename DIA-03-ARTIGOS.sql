<h2>Como reconquistar sua mente: 8 estratégias práticas</h2>

<h3>Estratégia 1: Ritual de Encerramento do Dia (não negociável)</h3>
<p><strong>Por que funciona:</strong> Cria um sinal psicológico claro de "o trabalho terminou".</p>

<p><strong>Como implementar (10 minutos):</strong></p>
<ol>
  <li><strong>Revisar o dia (2 min):</strong> O que foi feito? O que fica para amanhã?</li>
  <li><strong>Planejar o amanhã (3 min):</strong> Liste 3 prioridades para o próximo dia</li>
  <li><strong>Externalizar preocupações (2 min):</strong> Anote qualquer preocupação em um caderno - "está registrado, posso soltar"</li>
  <li><strong>Fechar aplicações (1 min):</strong> Literalmente feche email, Slack, todas as ferramentas de trabalho</li>
  <li><strong>Frase de encerramento (1 min):</strong> Diga em voz alta: "O trabalho de hoje está completo. Agora é hora de viver."</li>
</ol>

<p><strong>Dica:</strong> Faça isso sempre no mesmo horário e local. Seu cérebro aprenderá a associar esse ritual com desconexão.</p>

<h3>Estratégia 2: "Caixa de Preocupações" Mental</h3>
<p><strong>O que é:</strong> Uma técnica cognitiva para lidar com pensamentos invasivos sobre trabalho.</p>

<p><strong>Como praticar:</strong></p>
<p>Quando um pensamento sobre trabalho surgir fora do horário:</p>
<ol>
  <li><strong>Reconheça:</strong> "Ah, um pensamento sobre trabalho apareceu"</li>
  <li><strong>Avalie:</strong> "Isso requer ação imediata? (95% das vezes: não)"</li>
  <li><strong>Registre:</strong> Anote rapidamente em um caderno ou app</li>
  <li><strong>Agende:</strong> "Vou lidar com isso amanhã às 9h"</li>
  <li><strong>Solte:</strong> "Está registrado e agendado. Posso liberar isso agora."</li>
  <li><strong>Redirecione:</strong> Volte atenção para onde você está e o que está fazendo</li>
</ol>

<h3>Estratégia 3: Blocos de "Preocupação Programada"</h3>
<p><strong>Baseado em TCC:</strong> Em vez de tentar não pensar no trabalho (o que não funciona), programe horários específicos para isso.</p>

<p><strong>Como fazer:</strong></p>
<ul>
  <li>Reserve 15-20 minutos por dia para "pensar no trabalho deliberadamente"</li>
  <li>Durante esse tempo, permita-se preocupar, planejar, ruminar</li>
  <li>Quando pensamentos surgirem fora desse horário, diga: "Não agora. Vou pensar nisso no meu horário designado"</li>
</ul>

<p>Paradoxalmente, dar permissão limitada para preocupar-se reduz a preocupação geral.</p>

<h3>Estratégia 4: Restaure Limites Físicos e Digitais</h3>

<p><strong>Limites Digitais:</strong></p>
<ul>
  <li>Desinstale email corporativo do celular pessoal (ou use app que permite programar horários)</li>
  <li>Desative todas as notificações de trabalho após horário</li>
  <li>Use recursos como "Foco" no iPhone ou "Modo Trabalho/Pessoal" no Android</li>
  <li>Crie perfis separados no navegador: trabalho e pessoal</li>
</ul>

<p><strong>Limites Físicos (para home office):</strong></p>
<ul>
  <li>Trabalhe sempre no mesmo local da casa (se possível, não o quarto)</li>
  <li>Ao terminar o dia, "feche o escritório" fisicamente: arrume a mesa, feche o laptop</li>
  <li>Se trabalha no quarto, use biombos ou cortinas para "esconder" a área de trabalho</li>
  <li>Troque de roupa ao terminar o trabalho (sinal físico de transição)</li>
</ul>

<h3>Estratégia 5: Cultive "Experiências de Fluxo" Fora do Trabalho</h3>
<p><strong>Por que funciona:</strong> Seu cérebro pensa no trabalho porque não tem nada mais absorvente para processar.</p>

<p><strong>O que são experiências de fluxo:</strong> Atividades tão envolventes que você perde a noção do tempo e esquece preocupações.</p>

<p><strong>Exemplos:</strong></p>
<ul>
