<h2>O Método FOCCO e ansiedade</h2>

<p>O Método FOCCO aborda ansiedade de forma sistêmica:</p>

<h3>Identificação de gatilhos</h3>
<p>Mapeamos quais dos fatores estruturais mais afetam você especificamente.</p>

<h3>Práticas de regulação</h3>
<p>Mindfulness, respiração, técnicas somáticas adaptadas ao seu estilo de vida.</p>

<h3>Reorganização de vida</h3>
<p>Ajustamos rotina, ambiente e hábitos para minimizar fatores ansiogênicos.</p>

<h3>Desenvolvimento de significado</h3>
<p>Conexão com propósito maior reduz ansiedade existencial.</p>

<h3>Construção de resiliência</h3>
<p>Fortalecemos capacidade de tolerar incerteza e desconforto.</p>

<h2>Quando buscar ajuda profissional</h2>

<p>Busque suporte se:</p>
<ul>
<li>Ansiedade interfere significativamente com vida diária</li>
<li>Ataques de pânico frequentes</li>
<li>Comportamentos de evitação limitam sua vida</li>
<li>Sintomas físicos persistentes (taquicardia, falta de ar, tensão)</li>
<li>Pensamentos intrusivos constantes</li>
<li>Uso de substâncias para lidar com ansiedade</li>
</ul>

<p>Tratamentos eficazes existem: Terapia Cognitivo-Comportamental (TCC), EMDR, medicação quando necessário, mindfulness-based interventions.</p>

