<li>Recuperar-se rapidamente de perturbações emocionais</li>
<li>Responder conscientemente ao invés de reagir automaticamente</li>
<li>Manter perspectiva mesmo em situações difíceis</li>
<li>Acessar ampla gama de respostas emocionais apropriadas</li>
