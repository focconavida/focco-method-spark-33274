<h2>O que é equilíbrio emocional (e o que não é)</h2>

<h3>Equilíbrio emocional NÃO é:</h3>
<ul>
<li>Não sentir emoções intensas</li>
<li>Estar sempre calmo e feliz</li>
<li>Suprimir ou ignorar emoções difíceis</li>
