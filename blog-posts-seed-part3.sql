<h2>Exercícios práticos para começar hoje</h2>

<h3>1. Roda das emoções (manhã e noite)</h3>
<p>Use uma roda de emoções para identificar precisamente o que sente. Vai além de "estou mal" para "estou frustrado" ou "estou decepcionado".</p>

<h3>2. RAIN (4 minutos)</h3>
<p>Quando emoção intensa surgir:</p>
<ul>
<li><strong>R</strong>econheça: "Estou sentindo raiva"</li>
<li><strong>A</strong>ceite: "Está tudo bem sentir isso"</li>
<li><strong>I</strong>nvestigue: "Onde sinto no corpo? O que desencadeou?"</li>
<li><strong>N</strong>utra: "O que preciso agora?"</li>
</ul>

<h3>3. Janela de tolerância (automonitoramento)</h3>
<p>Identifique sua "janela de tolerância" - quando está dentro dela (regulado) vs. hiper ou hipo-arousal (desregulado). Aprenda seus sinais de saída da janela.</p>

<h3>4. Espaços de respiração de 3 minutos</h3>
<p>3x ao dia:</p>
<ul>
<li>1 min: Observe o que está acontecendo (pensamentos, emoções, sensações)</li>
<li>1 min: Foque apenas na respiração</li>
<li>1 min: Expanda consciência para corpo todo</li>
</ul>

<h3>5. Diário de gatilhos</h3>
<p>Durante 1 semana, anote:</p>
<ul>
<li>Situação que desencadeou emoção intensa</li>
<li>Emoção sentida</li>
<li>Pensamento que acompanhou</li>
<li>Como reagiu</li>
<li>Como gostaria de ter reagido</li>
</ul>

